#Used to undecrypt the name of the function to be found
hex = [0xE2, 0xCA, 0xDC, 0xE9, 0xCB, 0xDA, 0xFE, 0xDC, 0xC1, 0xCD, 0xCB, 0xCA, 0xDB, 
       0xDC, 0xCB, 0xEF, 0xCA, 0xCA, 0xDC, 0xCB, 0xDD, 0xDD, 0xAE, 0xAE, 0x00]
str=""
count=0
while count<0x18:
    str+= chr(hex[count]^0xAE)
    count+=1
    
